    private void textFieldActionPerformed(ActionEvent e)
    {
        JTextField field = (JTextField)e.getSource();
        if(field.getText().length() == 0)return;
        
        //this is the section panel. just trust me on this
        JPanel sectionPanel = (JPanel)field.getParent().getParent();
        
        //add a new "line"
        JPanel linePanel = getLine();
        sectionPanel.add(linePanel);
        
        validate();
        linePanel.getComponent(0).requestFocusInWindow();
    }


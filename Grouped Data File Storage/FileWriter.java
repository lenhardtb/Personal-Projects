    public void writeFile()
    {
        ArrayList<String> sections = new ArrayList<String>();
        ArrayList<ArrayList<String>> data = new ArrayList<ArrayList<String>>();
        
        //add type
        sections.add("Type");
        ArrayList<String> typeData = new ArrayList<String>();
        typeData.add(typeBox.getSelectedItem().toString());
        data.add(typeData);
        
        //add sections and data from panel
        for(int i = 0; i < panel.getComponentCount(); i++)
        {
            JPanel sectionPanel = (JPanel)panel.getComponent(i);
            TitledBorder border = (TitledBorder)sectionPanel.getBorder();
            sections.add(border.getTitle());
            
            ArrayList<String> sectionData = new ArrayList<String>();
            for(int o = 0; o < sectionPanel.getComponentCount(); o++)
            {
                JPanel linePanel = (JPanel)sectionPanel.getComponent(o);
                JTextField field = (JTextField)linePanel.getComponent(0);//zero is intentional
                if(!field.getText().equals(""))
                    sectionData.add(field.getText());
            }
            data.add(sectionData);
        }
        
        //actually write file from gathered data
        FileAssist test = new FileAssist(filenameTextField.getText());
        test.createFile();
        for(int i = 0; i < sections.size(); i++)
        {
            test.addSection(sections.get(i));
            test.setLinesIn(sections.get(i), data.get(i));
        }
    }
    

    private void filenameTextFieldActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_filenameTextFieldActionPerformed
    {//GEN-HEADEREND:event_filenameTextFieldActionPerformed
        FileAssist file = new FileAssist(filenameTextField.getText());
        if(file.exists())
        {
            //figure out type
            String typeS = file.getLineIn("Type");
            int type = 0;
            for(int i = 0; i < types.length; i++)
                if(types[i].equals(typeS))
                {
                    type = i;
                    break;
                }
            typeBox.setSelectedIndex(type);
            panel.removeAll();
            
            
            ArrayList<String> sections = file.getSections();
            for(int i = 1; i < sections.size(); i++)//starts at 1, skips Type
            {
                JPanel sectionPanel = getSection(sections.get(i));
                
                ArrayList<String> lines = file.getLinesIn(sections.get(i));
                for(int o = 0; o < lines.size(); o++)
                {
                    JTextField field = (JTextField)((JPanel)(sectionPanel.getComponent(o))).getComponent(0);
                    field.setText(lines.get(o));
                    
                    sectionPanel.add(getLine());
                }
                
                panel.add(sectionPanel);
            }
            validate();
        }//if file exists
    }//GEN-LAST:event_filenameTextFieldActionPerformed

    private void typeBoxActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_typeBoxActionPerformed
    {//GEN-HEADEREND:event_typeBoxActionPerformed
        int type = typeBox.getSelectedIndex();
        
        panel.removeAll();
        
        String[] sections = typeSections[type];
        for(String section: sections)
        {
            panel.add(getSection(section));
        }
        validate();
    }//GEN-LAST:event_typeBoxActionPerformed

    
    private JPanel getSection(String title)
    {
        JPanel sectionPanel = new JPanel(new GridLayout(0,1));
        sectionPanel.setBorder(new TitledBorder(title));
        sectionPanel.add(getLine());
        return sectionPanel;
    }
    
    private JPanel getLine()
    {
        JPanel linePanel = new JPanel(new FlowLayout(FlowLayout.LEFT));
          JTextField field = new JTextField(20);
          field.addActionListener(new ActionListener()
          {
              public void actionPerformed(ActionEvent e)
              {
                  textFieldActionPerformed(e);
              }
          });
         linePanel.add(field);
         return linePanel;
    }
    
